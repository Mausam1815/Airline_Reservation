    private void bookFlightButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookFlightButtonActionPerformed
        try {
            // TODO add your handling code here:
            String CustomerID = custID.getText();
            String FirstName = firstName.getText();
            String LastName = lastName.getText();
            String Contact = contact.getText();
            String Gender = gender.getText();
            
            String Arrival = from.getSelectedItem().toString();
            String Deeparture = to.getSelectedItem().toString();
            
            String TicketID = ticketID.getText();
            
            Connection connection;
            PreparedStatement preparedStatement;
            Class.forName("com.mysql.cj.jdbc.Driver");
            connection = DriverManager.getConnection("jdbc:mysql://localhost/airlinedb", "root", "sqlworkbench@123");
            preparedStatement = connection.prepareStatement("Insert into ticket (TicketID, FlightID, CustomerID, Arrival, Departure, FirstName, LastName, Gender, Contact) values (?,?,?,?,?,?,?,?,?);");
            preparedStatement.setString(1, TicketID);
            preparedStatement.setString(2, flightID);
            preparedStatement.setString(3, CustomerID);
            preparedStatement.setString(4, Arrival);
            preparedStatement.setString(5, Deeparture);
            preparedStatement.setString(6, FirstName);
            preparedStatement.setString(7, LastName);
            preparedStatement.setString(8, Gender);
            preparedStatement.setString(9, Contact);
            preparedStatement.executeUpdate();
            
            JOptionPane.showMessageDialog(null,"Ticket booked successfully");
            this.setVisible(false);
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(BookFlight.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bookFlightButtonActionPerformed


    private void searchFlightButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchFlightButtonActionPerformed
        try {
            // TODO add your handling code here:
            String arrival = from.getSelectedItem().toString();
            String departure = to.getSelectedItem().toString();
                        
            Connection connection;
            PreparedStatement preparedStatement;
            Class.forName("com.mysql.cj.jdbc.Driver");
            connection = DriverManager.getConnection("jdbc:mysql://localhost/airlinedb", "root", "sqlworkbench@123");
            preparedStatement = connection.prepareStatement("Select * from flight where Arrival = ? and Departure = ?;");
            preparedStatement.setString(1, arrival);
            preparedStatement.setString(2, departure);
            
            ResultSet resultSet = preparedStatement.executeQuery();
            ResultSetMetaData metaData = resultSet.getMetaData();
            int colCount = metaData.getColumnCount();
            DefaultTableModel dtm = (DefaultTableModel) flightTable.getModel();
            while(resultSet.next()) {
                Vector vector = new Vector();
                for(int i=1; i<=colCount; i++) {
                    vector.add(resultSet.getString("FlightID"));
                    vector.add(resultSet.getString("FlightName"));
                    vector.add(resultSet.getString("Arrival"));
                    vector.add(resultSet.getString("Departure"));
                    vector.add(resultSet.getString("Duration"));
                    vector.add(resultSet.getString("Date"));
                }
                dtm.addRow(vector);;
                
            }
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(BookFlight.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_searchFlightButtonActionPerformed


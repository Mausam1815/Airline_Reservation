    public void autoID() {
        try {
            Connection connection;
            PreparedStatement preparedStatement;
            Class.forName("com.mysql.cj.jdbc.Driver");
            connection = DriverManager.getConnection("jdbc:mysql://localhost/airlinedb", "root", "sqlworkbench@123");
            preparedStatement = connection.prepareStatement("Select MAX(FlightID) from Flight;");
            ResultSet resultSet = preparedStatement.executeQuery();
            resultSet.next();
            if(resultSet.getString("MAX(FlightID)") == null) {
                flightID.setText("FL001");
            }else {
                long id = Long.parseLong(resultSet.getString("MAX(FlightID)").substring(2,  resultSet.getString("MAX(flightID)").length()));
                id++;
                flightID.setText("FL" + String.format("%03d", id));
            }
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


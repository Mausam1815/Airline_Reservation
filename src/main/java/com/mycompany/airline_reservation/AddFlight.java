    private void addFlightButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addFlightButtonActionPerformed
        // TODO add your handling code here:
        try {
            String FlightId = flightID.getText();
            String FlightName = flightName.getText();
            String Arrival = arrival.getText();
            String Departure = departure.getText();
            String Duration = duration.getText();
            String Seats = seats.getText();
            String Fare = fare.getText();
            
            DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String Date = dateFormat.format(dof.getDate());
            
            Connection connection;
            PreparedStatement preparedStatement;
            Class.forName("com.mysql.cj.jdbc.Driver");
            connection = DriverManager.getConnection("jdbc:mysql://localhost/airlinedb", "root", "sqlworkbench@123");
            preparedStatement = connection.prepareStatement("Insert into flight (FlightID, FlightName, Arrival, Departure, Duration, Seats, Fare, Date) values (?,?,?,?,?,?,?,?);");
            preparedStatement.setString(1, FlightId);
            preparedStatement.setString(2, FlightName);
            preparedStatement.setString(3, Arrival);
            preparedStatement.setString(4, Departure);
            preparedStatement.setString(5, Duration);
            preparedStatement.setString(6, Seats);
            preparedStatement.setString(7, Fare);
            preparedStatement.setString(8, Date);
            
            preparedStatement.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Flight Added Successfully");
            this.setVisible(false);
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddFlight.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addFlightButtonActionPerformed

